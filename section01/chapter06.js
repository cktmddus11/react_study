// 형변환(Type Casting)
/* - 묵시적 형변환
        개발자가 직접 설정하지 않아도 
        알아서 자바스크립트 엔진이 형변환 하는 것을 말함.

    - 명시적 형 변환
        개발자가 직접 함수 등을 이용해 
        형 변환을 이용함.

*/

// 묵시적 형변환
let num = 10;
let str = "20";
const result = num + str;
console.log(result); // 1020 
// 묵시적으로 num이라는 숫자값이 string 으로 형변환됨.



